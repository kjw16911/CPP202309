#include <iostream>
using namespace std;

int main()
{
	int choice;

	cout << "1. 파일 저장" << endl;
	cout << "2. 저장 없이 닫기" << endl;
	cout << "3. 종료" << endl;
	cin >> choice;

	if (choice == 1)
		cout << "파일 저장을 선택했습니다." << endl;
	else if (choice == 2)
		cout << "파일 닫기를 선택했습니다." << endl;
	else if (choice == 3)
		cout << "프로그램을 종료합니다." << endl;
	else
		cout << "잘못된 선택입니다." << endl;
	return 0;
//if문을 여러 개 사용할 경우 각각의 if문을 실행하기 때문에
//한 조건문에서 여러 조건을 사용하려면 else if를 사용할 것.
}